// Helper function to convert static data to CountryData format
export function convertToCountryData(
  data: { [country: string]: { [year: number]: number } }
): { year: number; [country: string]: number | string }[] {
  const years = new Set<number>();
  Object.values(data).forEach(countryData => {
    Object.keys(countryData).forEach(year => years.add(parseInt(year)));
  });

  const sortedYears = Array.from(years).sort((a, b) => a - b);
  
  return sortedYears.map(year => {
    const row: { year: number; [country: string]: number | string } = { year };
    Object.entries(data).forEach(([country, countryData]) => {
      if (countryData[year] !== undefined) {
        row[country] = countryData[year];
      }
    });
    return row;
  });
}

// Helper function to get single year data for bar charts
export function getSingleYearData(
  data: { [country: string]: { [year: number]: number } },
  year: number
): { country: string; value: number }[] {
  return Object.entries(data)
    .filter(([, countryData]) => countryData[year] !== undefined)
    .map(([country, countryData]) => ({
      country,
      value: countryData[year],
    }))
    .sort((a, b) => b.value - a.value);
}

// Helper to get available years from data
export function getAvailableYears(
  data: { [country: string]: { [year: number]: number } }
): number[] {
  const years = new Set<number>();
  Object.values(data).forEach(countryData => {
    Object.keys(countryData).forEach(year => years.add(parseInt(year)));
  });
  return Array.from(years).sort((a, b) => a - b);
}
